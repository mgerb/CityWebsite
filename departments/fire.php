	<h1 class="text-center header-text">Fire Department</h1>

	<p class="p-big">Our fire department has twenty-five volunteer fire fighters with ten being first responders and four being EMT's. 
	Our coverage area is 150 square miles including seven townships and 900 households.

	<p class="p-big">In 2000 the City of Grand Meadow built a new Emergency Services Building to house the fire department, ambulance service, 
	and police department due to the growth of each of our departments and the inconvenience of each department being in their own building.

