	<span class="p-big">The Grand Meadow EDA meets the first Tuesday of every 
month at 3:00pm at the Grand Meadow City Hall.</span><br><br>

	<span class="p-big">1. Primary purpose is to serve as an Economic Development Authority.</span><br>
	<span class="p-big">2. The affairs of the Authority are to promote the growth and development of commercial and industrial concerns in Grand Meadow.</span> <br>
	<span class="p-big">3. Provide financial assistance through EDA Revolving Loan Funds, Small Cities Development Program Income Dollars, TIF (tax increment financing), Tax Abatement, and JOB-Z (tax benefits).</span><br>
	<span class="p-big">4. Promote job creation and job retention.</span><br><br>
